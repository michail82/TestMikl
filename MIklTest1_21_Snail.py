h = int( input() )  # H - Высота шеста
a = int( input() )  # A - Подем метров за день
b = int( input() )  # B - Спуск метров за ночь
print( (h - a - 1) // (a - b) + 2 )
print( (h - a) // (a - b) + 1 )
