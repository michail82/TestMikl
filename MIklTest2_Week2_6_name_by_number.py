# Мы хотим по введенному числу выводить его
# текстовое название на английском языке,
# хотя бы для чисел 1, 2 и 3

# Пример ELif
x = int( input() )
if x == 1:
    print( 'One' )
elif x == 2:
    print( 'Two' )
elif x == 3:
    print( 'Three' )
else:
    print( 'Other' )

